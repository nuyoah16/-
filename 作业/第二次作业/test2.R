#编写一个函数，输入a,b,c值构成的矩阵，返回每行值对应的解
quaroot=function(x){
  n=nrow(x)
  ans=matrix(NA,n,2)#构造结果矩阵
#传入n*3矩阵
  for(i in 1:n){
    a=x[i,1]
    b=x[i,2]
    c=x[i,3]
    if(a==0){
      x1=x2=-c/b#方便起见，不考虑b=0的情况
      ans[i,1]=x1
      ans[i,2]=x2
    }else{
      if(b^2-4*a*c<0){
        ans[i,1]=NA
        ans[i,2]=NA
      }else{
        x1=(-b+sqrt(b^2-4*a*c))/2*a
        x2=(-b-sqrt(b^2-4*a*c))/2*a
        ans[i,1]=x1
        ans[i,2]=x2
      }
    }
  }
  return(ans)
}
quaroot(matrix(1:9,3,3))
quaroot(matrix(c(1,0,2,3,1,1),2,3))
quaroot(matrix(c(1,1,1,12,4,-6,25,-10,-11),3,3))
quaroot(matrix(c(1,1,-2,-5,1,6),2,3))

